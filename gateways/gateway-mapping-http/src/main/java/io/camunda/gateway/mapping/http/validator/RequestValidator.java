package io.camunda.gateway.mapping.http.validator;

import static io.camunda.gateway.mapping.http.validator.ErrorMessages.ERROR_MESSAGE_DATE_PARSING;
import static io.camunda.gateway.mapping.http.validator.ErrorMessages.ERROR_MESSAGE_DURATION_PARSING;
import static io.camunda.gateway.mapping.http.validator.ErrorMessages.ERROR_MESSAGE_ILLEGAL_CHARACTER;
import static io.camunda.gateway.mapping.http.validator.ErrorMessages.ERROR_MESSAGE_INVALID_ATTRIBUTE_VALUE;
import static io.camunda.gateway.mapping.http.validator.ErrorMessages.ERROR_MESSAGE_INVALID_DECISION_EVALUATION_INSTANCE_KEY_FORMAT;
import static io.camunda.gateway.mapping.http.validator.ErrorMessages.ERROR_MESSAGE_INVALID_KEY_FORMAT;
import static io.camunda.gateway.mapping.http.validator.ErrorMessages.ERROR_MESSAGE_TOO_MANY_CHARACTERS;
import static io.camunda.zeebe.protocol.record.RejectionType.INVALID_ARGUMENT;

import io.camunda.gateway.mapping.http.GatewayErrorMapper;
import io.camunda.gateway.mapping.http.util.KeyUtil;
import io.camunda.gateway.protocol.model.Changeset;
import java.time.Duration;
import java.time.OffsetDateTime;
import java.time.format.DateTimeParseException;
import java.util.ArrayList;
import java.util.List;
import java.util.Optional;
import java.util.function.Consumer;
import java.util.function.Supplier;
import java.util.regex.Pattern;
import org.springframework.http.HttpStatus;
import org.springframework.http.ProblemDetail;

public final class RequestValidator {

  /** Compiled pattern for a valid XML ID (xsd:ID / NCName), used for BPMN and DMN identifiers. */
  public static final Pattern XML_ID_PATTERN = Pattern.compile("^[\\p{L}_][\\p{L}\\p{N}_\\-.]*$");

  /**
   * Compiled pattern for a valid {@code decisionEvaluationInstanceKey}. The key is generated by the
   * engine ({@code DecisionEvaluationInstanceKeyGenerator}) using the format {@code %d-%d}
   * (decision evaluation key followed by a 1-based index of the evaluated decision).
   */
  public static final Pattern DECISION_EVALUATION_INSTANCE_KEY_PATTERN =
      Pattern.compile("^\\d+-\\d+$");

  /** Maximum allowed length for a business ID, matching the engine-side constraint. */
  public static final int MAX_BUSINESS_ID_LENGTH = 256;

  public static Optional<ProblemDetail> createProblemDetail(final List<String> violations) {
    String problems = String.join(". ", violations);
    if (!problems.endsWith(".")) {
      problems = problems + ".";
    }
    return violations.isEmpty()
        ? Optional.empty()
        : Optional.of(
            GatewayErrorMapper.createProblemDetail(
                HttpStatus.BAD_REQUEST, problems, INVALID_ARGUMENT.name()));
  }

  public static OffsetDateTime validateDate(
      final String dateString, final String attributeName, final List<String> violations) {
    if (dateString != null && !dateString.isEmpty()) {
      try {
        return OffsetDateTime.parse(dateString);
      } catch (final DateTimeParseException ex) {
        violations.add(ERROR_MESSAGE_DATE_PARSING.formatted(attributeName, dateString));
      }
    }
    return null;
  }

  public static Duration validateDuration(
      final String durationString, final String attributeName, final List<String> violations) {
    if (durationString != null && !durationString.isEmpty()) {
      try {
        return Duration.parse(durationString);
      } catch (final DateTimeParseException ex) {
        violations.add(ERROR_MESSAGE_DURATION_PARSING.formatted(attributeName, durationString));
      }
    }
    return null;
  }

  public static boolean isEmpty(final Changeset changeset) {
    return changeset == null
        || (changeset.getFollowUpDate() == null
            && changeset.getDueDate() == null
            && changeset.getCandidateGroups() == null
            && changeset.getCandidateUsers() == null
            && changeset.getPriority() == null);
  }

  public static Optional<ProblemDetail> validate(final Consumer<List<String>> customValidation) {
    final List<String> violations = new ArrayList<>();

    customValidation.accept(violations);

    return createProblemDetail(violations);
  }

  public static Optional<ProblemDetail> validate(final Supplier<List<String>> customValidation) {
    return createProblemDetail(customValidation.get());
  }

  public static void validateOperationReference(
      final Long operationReference, final List<String> violations) {
    if (operationReference != null && operationReference < 1) {
      violations.add(
          ERROR_MESSAGE_INVALID_ATTRIBUTE_VALUE.formatted(
              "operationReference", operationReference, "> 0"));
    }
  }

  /**
   * Validates that a string-encoded key can be parsed as a valid Long.
   *
   * @param keyValue the string value to validate
   * @param fieldName the name of the field for error messaging
   * @param violations the list to add validation errors to
   */
  public static void validateKeyFormat(
      final String keyValue, final String fieldName, final List<String> violations) {
    if (keyValue != null && KeyUtil.tryParseLong(keyValue).isEmpty()) {
      violations.add(ERROR_MESSAGE_INVALID_KEY_FORMAT.formatted(fieldName, keyValue));
    }
  }

  /**
   * Validates that a {@code decisionEvaluationInstanceKey} matches the expected format produced by
   * the engine: {@code <decisionEvaluationKey>-<index>} where both parts are non-negative integers
   * (e.g. {@code "2251799813684367-1"}).
   *
   * @param keyValue the value to validate (may be null; null is not validated)
   * @param violations the list to add validation errors to
   */
  public static void validateDecisionEvaluationInstanceKeyFormat(
      final String keyValue, final List<String> violations) {
    if (keyValue != null && !DECISION_EVALUATION_INSTANCE_KEY_PATTERN.matcher(keyValue).matches()) {
      violations.add(
          ERROR_MESSAGE_INVALID_DECISION_EVALUATION_INSTANCE_KEY_FORMAT.formatted(
              "decisionEvaluationInstanceKey",
              keyValue,
              DECISION_EVALUATION_INSTANCE_KEY_PATTERN.pattern()));
    }
  }

  /**
   * Validates that a process definition ID matches the BPMN identifier pattern.
   *
   * @param processDefinitionId the value to validate (may be null; null is not validated)
   * @param violations the list to add validation errors to
   */
  public static void validateProcessDefinitionId(
      final String processDefinitionId, final List<String> violations) {
    if (processDefinitionId != null && !XML_ID_PATTERN.matcher(processDefinitionId).matches()) {
      violations.add(
          ERROR_MESSAGE_ILLEGAL_CHARACTER.formatted(
              "processDefinitionId", XML_ID_PATTERN.pattern()));
    }
  }

  /**
   * Validates that a decision definition ID matches the XML ID (NCName) pattern.
   *
   * @param decisionDefinitionId the value to validate (may be null; null is not validated)
   * @param violations the list to add validation errors to
   */
  public static void validateDecisionDefinitionId(
      final String decisionDefinitionId, final List<String> violations) {
    if (decisionDefinitionId != null && !XML_ID_PATTERN.matcher(decisionDefinitionId).matches()) {
      violations.add(
          ERROR_MESSAGE_ILLEGAL_CHARACTER.formatted(
              "decisionDefinitionId", XML_ID_PATTERN.pattern()));
    }
  }

  /**
   * Validates that a business ID does not exceed the maximum allowed length.
   *
   * @param businessId the business ID to validate (may be null; null is not validated)
   * @param violations the list to add validation errors to
   */
  public static void validateBusinessId(final String businessId, final List<String> violations) {
    if (businessId != null && businessId.length() > MAX_BUSINESS_ID_LENGTH) {
      violations.add(
          ERROR_MESSAGE_TOO_MANY_CHARACTERS.formatted("businessId", MAX_BUSINESS_ID_LENGTH));
    }
  }
}

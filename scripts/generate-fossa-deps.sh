#!/usr/bin/env bash
set -euo pipefail

usage() {
  cat <<'EOF'
Generate a FOSSA manual dependency file from depgraph-maven-plugin text output.

Usage:
  ./scripts/generate-fossa-deps.sh [--depgraph-file <path>] [--output <path>]

Examples:
  ./mvnw com.github.ferstl:depgraph-maven-plugin:4.0.1:aggregate \
    -f "./pom.xml" \
    -DgraphFormat=text \
    -DmergeScopes \
    -DreduceEdges=false \
    -DshowVersions=true \
    -DshowGroupIds=true \
    -DshowOptional=true \
    -DrepeatTransitiveDependenciesInTextGraph=false > depgraph.txt

  ./scripts/generate-fossa-deps.sh --depgraph-file depgraph.txt

  ./mvnw com.github.ferstl:depgraph-maven-plugin:4.0.1:aggregate \
    -f "./pom.xml" \
    -DgraphFormat=text \
    -DmergeScopes \
    -DreduceEdges=false \
    -DshowVersions=true \
    -DshowGroupIds=true \
    -DshowOptional=true \
    -DrepeatTransitiveDependenciesInTextGraph=false | \
    ./scripts/generate-fossa-deps.sh

Options:
  --depgraph-file <path>  Read depgraph text from a file instead of stdin.
  --output <path>         Output path for the generated FOSSA dependency file.
                          Defaults to <repo-root>/fossa-deps.yml
  --help                  Show this help message.

The script expects depgraph text output that was generated after any required
repo-specific POM adjustments have already been applied. It emits unique
external Maven dependencies from the graph, including transitives.
EOF
}

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/.." && pwd)"
OUTPUT_FILE="${REPO_ROOT}/fossa-deps.yml"
DEPGRAPH_FILE=""
WORK_DIR="$(mktemp -d)"

cleanup() {
  rm -rf "${WORK_DIR}"
}

trap cleanup EXIT

while [[ $# -gt 0 ]]; do
  case "$1" in
    --depgraph-file)
      DEPGRAPH_FILE="$2"
      shift 2
      ;;
    --output)
      OUTPUT_FILE="$2"
      shift 2
      ;;
    --help|-h)
      usage
      exit 0
      ;;
    *)
      echo "Unknown argument: $1" >&2
      usage >&2
      exit 1
      ;;
  esac
done

for required_command in awk grep sort; do
  if ! command -v "${required_command}" >/dev/null 2>&1; then
    echo "${required_command} is required to generate the FOSSA dependency file." >&2
    exit 1
  fi
done

mkdir -p "$(dirname "${OUTPUT_FILE}")"

if [[ -z "${DEPGRAPH_FILE}" ]]; then
  if [[ -t 0 ]]; then
    echo "Provide depgraph input via --depgraph-file or stdin." >&2
    usage >&2
    exit 1
  fi

  DEPGRAPH_FILE="${WORK_DIR}/dependency-graph.txt"
  cat > "${DEPGRAPH_FILE}"
fi

EXTRACTED_DEPENDENCIES_FILE="${WORK_DIR}/dependencies.txt"

grep -Eo '[^:[:space:]]+:[^:[:space:]]+:[^:[:space:]]+:[^()[:space:]]+' "${DEPGRAPH_FILE}" | \
  awk -F: '
    $1 ~ /^io\.camunda/ {
      next
    }
    {
      include_dependency = 0
      split($4, scopes, ",")
      for (scope_index in scopes) {
        scope = scopes[scope_index]
        if (scope != "import" && scope != "provided" && scope != "system" && scope != "test") {
          include_dependency = 1
        }
      }

      if (include_dependency) {
        print $1 ":" $2 ":" $3
      }
    }
  ' | \
  LC_ALL=C sort -u > "${EXTRACTED_DEPENDENCIES_FILE}"

if [[ ! -s "${EXTRACTED_DEPENDENCIES_FILE}" ]]; then
  echo "No external Maven dependencies were found in the depgraph output." >&2
  exit 1
fi

{
  echo "# Generated by ./scripts/generate-fossa-deps.sh"
  echo "# Derived from depgraph-maven-plugin text output."
  echo "referenced-dependencies:"

  while IFS=: read -r group artifact version; do
    escaped_version="${version//\\/\\\\}"
    escaped_version="${escaped_version//\"/\\\"}"
    echo "  - type: maven"
    echo "    name: ${group}:${artifact}"
    echo "    version: \"${escaped_version}\""
  done < "${EXTRACTED_DEPENDENCIES_FILE}"
} > "${OUTPUT_FILE}"

echo "Generated ${OUTPUT_FILE}"
